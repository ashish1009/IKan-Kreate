  void Renderer3D::Shutdown()
  {
    IK_PROFILE();
    IK_LOG_INFO(LogModule::Renderer3D, "Destroying 3D Renderer for Fullscreen Cube");
    s_data.pipeline.reset();
    s_data.vertexBuffer.reset();
  }
  
  void Renderer3D::DrawFullscreenCube()
  {
    IK_PERFORMANCE("Renderer3D::DrawFullscreenCube");
    Renderer::DrawFullscreenCube(s_data.pipeline);
  }
